abstract class BaseTextBuild extends StatelessWidget {
   final String data;
  final Key key;
  final TextStyle style;
  final TextStyle defaultTextSyle;
  final StrutStyle strutStyle;
  final TextAlign textAlign;
  final TextDirection textDirection;
  final bool softWrap;
  final TextOverflow overflow;
  final double textScaleFactor;
  final bool corPrimaria;
  final Color customColor;
  final Color corDarkMode;
  final Color corLighMode;
  final Color corShadow;
  final bool showSadow;
  final int maxLines;
  final FontWeight weight;

  Widget buildText(BuildContext context,{@required TextStyle defaultTextSyle}) {
    bool isDark = AppUtil().appBrightness == Brightness.dark;
    var mostrarShadow = showSadow ?? false;
    return Text(
      data,
      style: style != null ? style.copyWith(
         color: (corDarkMode != null || corLighMode != null )  ? (isDark ? corDarkMode:corLighMode)  : corPrimaria != null && corPrimaria
            ? Theme.of(context).primaryColor
            : customColor,
            fontWeight: this.weight,
            shadows: mostrarShadow ? [
              Shadow(
                  offset: Offset(10.0, 10.0),
                  blurRadius: 3.0,
                  color: Color.fromARGB(255, 0, 0, 0),
                )
            ]:null
      ) : defaultTextSyle.copyWith(
        color: (corDarkMode != null || corLighMode != null )  ? (isDark ? corDarkMode:corLighMode)  : corPrimaria != null && corPrimaria
            ? Theme.of(context).primaryColor
            : customColor,
            fontWeight: this.weight,
             shadows: mostrarShadow ? [
              Shadow(
                  offset: Offset(2.0, 2.0),
                  blurRadius: 3.0,
                  color: corShadow ?? Color.fromARGB(255, 0, 0, 0),
                )
            ]:null
      ),
    
      textAlign: textAlign,
      softWrap: softWrap,
      maxLines: maxLines,
      strutStyle: strutStyle,
      overflow: overflow,
      textScaleFactor: textScaleFactor,
    );//.tr(context: context);
  }

    const BaseTextBuild(
      this.data,
      this.corPrimaria,
      this.customColor,
      this.key,
      this.style,
      this.strutStyle,
      this.textAlign,
      this.textDirection,
      this.softWrap,
      this.overflow,
      this.textScaleFactor,
      this.maxLines,
      this.defaultTextSyle, 
      this.weight, 
      this.corDarkMode, 
      this.corLighMode,
      this.showSadow,
      this.corShadow);
}
